import java.util.Scanner

fun main() {
    var apple = Scanner(System.`in`)

    println("Enter your name:")
    var name = readln()
    println("My name is $name")

    println("Enter a number:")
    var num = readln()
    println("Entered $num")

    println("Enter a float/decimal:")
    var number = apple.nextFloat()
    println("Entered $number")
}